## This function gets a matrix and stores both the matrix (in x) 
## and its inverse (m)


makeCacheMatrix <- function(x = matrix()) {
  
  m <- NULL # "placeholder" for the calculated inverse
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setInverse <- function(solve) m <<- solve
  getInverse <- function() m
  list(set = set, get = get,
       setInverse = setInverse,
       getInverse = getInverse)
}


## gets a matrix and calculates its inverse if it hasn't already been done
## 

cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  m <- x$getInverse()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setInverse(m)
  m
}